package chapter2;

public class DoubleEx1 {

	public static void main(String[] args) {
		// 실수형 데이터 타입은 부동 소수점 방식으로 표현한다.
		// float(4), double(8): 기준
		// 실수값을 사용하면 double 데이터 타입으로 처리 및 인식된다
		
		double dnum = 3.14;
		float fnum = 3.14F;
		
		System.out.println(dnum);
		System.out.println(fnum);

	}

}
