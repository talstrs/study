package chapter2;

public class Variable1 {

	public static void main(String[] args) {
		
		// 변수: 기억 장소를 가리키는 이름
		// 데이터 타입: 자료형
		
		/*
		  var 변수; 
		  let 변수; 
		  const 변수;
		 */
		
		// 자바 변수 선언 규칙
		// 데이터 타입 변수이름;
		// 데이터 타입 기능? 메모리 상의 기억 장소를 크기와 형태로 생성
		// 크기: 1, 2, 4, 8 바이트
		// 형태: 정수형, 실수형, 논리형, 문자형 등
		int level; //변수 선언(정의)
		level = 10;
		
		//System.out.println() - () 안의 데이터 또는 변수의 값을 출력하는 기능
		System.out.println(level);
	}

}
